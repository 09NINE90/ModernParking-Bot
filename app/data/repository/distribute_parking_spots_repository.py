async def get_dates_with_availability(cur):
    """
        Получает список дат, на которые есть доступные парковочные места и ожидающие запросы.

        Выполняет поиск дат, где одновременно выполняются два условия:
        1. Есть освобожденные парковочные места, которые еще не заняты
        2. Есть pending-запросы на парковку на эти же даты

        Параметры:
            cur: курсор базы данных для выполнения SQL-запросов

        Возвращает:
            list: список объектов datetime.date - даты, когда возможна автоматическая
                  выдача парковочных мест (есть и свободные места, и запросы на них)

        Логика:
            - Основной запрос ищет даты из parking_releases где user_id_took IS NULL
              (места свободны)
            - Подзапрос EXISTS проверяет, что на эти же даты есть запросы со статусом 'PENDING'
            - DISTINCT гарантирует уникальность дат в результате

        Особенности:
            - Функция используется для поиска дат, когда система может автоматически
              распределить свободные места среди ожидающих пользователей
            - Возвращает только даты, где есть и предложение (свободные места), и спрос (запросы)
            - Асинхронная функция, требует await при вызове
        """
    cur.execute('''
                SELECT DISTINCT pr.release_date
                FROM dont_touch.parking_releases pr
                WHERE pr.user_id_took IS NULL
                  AND EXISTS (SELECT 1
                              FROM dont_touch.parking_requests prq
                              WHERE prq.request_date = pr.release_date
                                AND prq.status = 'PENDING')
                ''')

    return [row[0] for row in cur.fetchall()]

async def get_free_spots(cur, distribution_date):
    """
        Получает список свободных парковочных мест на указанную дату.

        Выполняет поиск всех освобожденных парковочных мест на конкретную дату,
        которые еще не были заняты другими пользователями.

        Параметры:
            cur: курсор базы данных для выполнения SQL-запросов
            distribution_date: дата, на которую запрашиваются свободные места

        Возвращает:
            list: список словарей, где каждый словарь содержит:
                - 'id': UUID записи в таблице parking_releases
                - 'spot_id': идентификатор парковочного места

        Логика:
            - Ищет записи в parking_releases где release_date совпадает с указанной датой
            - Фильтрует только свободные места (user_id_took IS NULL)
            - Сортирует результаты по времени создания (created_at) в порядке возрастания
              для обеспечения детерминированного порядка распределения

        Особенности:
            - Возвращает именно свободные места (никем не занятые)
            - ORDER BY created_at ASC обеспечивает fair distribution - первыми
              распределяются места, которые были освобождены раньше
            - Асинхронная функция, требует await при вызове
            - Используется в процессах автоматического распределения мест
        """
    cur.execute('''
                SELECT id, spot_id
                FROM dont_touch.parking_releases
                WHERE release_date = %s
                  AND user_id_took IS NULL
                ORDER BY created_at ASC
                ''', (distribution_date,))
    return cur.fetchall()

async def get_candidates(cur, distribution_date, free_spots):
    """
        Получает список кандидатов для распределения свободных мест на указанную дату.

        Выбирает пользователей с pending-запросами на парковку, упорядоченных по рейтингу,
        для последующего распределения среди них доступных свободных мест.

        Параметры:
            cur: курсор базы данных для выполнения SQL-запросов
            distribution_date: дата, на которую распределяются места
            free_spots: список свободных мест (используется для определения лимита кандидатов)

        Возвращает:
            list: список словарей, где каждый словарь содержит:
                - 'request_id': UUID запроса на парковку
                - 'user_id': UUID пользователя
                - 'rating': рейтинг пользователя (числовое значение)
                - 'tg_id': идентификатор пользователя в Telegram

        Логика:
            - Выбирает pending-запросы на указанную дату
            - Присоединяет данные пользователей для получения рейтинга и tg_id
            - Сортирует по рейтингу в порядке возрастания (низший рейтинг первый)
            - Ограничивает количество кандидатов количеством свободных мест

        Особенности:
            - ORDER BY u.rating ASC - приоритет отдается пользователям с НИЗШИМ рейтингом
              (вероятно, система справедливого распределения, где меньше получившие)
            - LIMIT %s - количество кандидатов равно количеству свободных мест
            - Статус 'PENDING' - рассматриваются только активные, необработанные запросы
            - Асинхронная функция, требует await при вызове
        """
    cur.execute('''
                SELECT prq.id as request_id, prq.user_id, u.rating, u.tg_id
                FROM dont_touch.parking_requests prq
                         JOIN dont_touch.users u ON prq.user_id = u.user_id
                WHERE prq.request_date = %s
                  AND prq.status = 'PENDING'
                ORDER BY u.rating ASC
                LIMIT %s
                ''', (distribution_date, len(free_spots)))
    return cur.fetchall()

async def get_release_owner(cur, release_id):
    """
       Получает информацию о пользователе, который освободил парковочное место.

       Находит владельца (инициатора освобождения) парковочного места по идентификатору
       записи в таблице parking_releases.

       Параметры:
           cur: курсор базы данных для выполнения SQL-запросов
           release_id: UUID записи в таблице parking_releases

       Возвращает:
           dict или None:
               - Словарь с полями 'user_id' (UUID владельца) и 'tg_id' (Telegram ID владельца)
               - None, если запись с указанным release_id не найдена

       Логика:
           - Ищет запись в parking_releases по первичному ключу (id)
           - Присоединяет таблицу users чтобы получить Telegram ID владельца
           - Возвращает идентификаторы как внутренние (user_id), так и внешние (tg_id)

       Особенности:
           - Используется для уведомления первоначального владельца о событиях с его местом
           - Возвращает именно того, кто ОСВОБОДИЛ место (user_id), а не того, кто его занял (user_id_took)
           - Асинхронная функция, требует await при вызове
           - Полезно для систем уведомлений и аудита действий
       """
    cur.execute('''
                SELECT pr.user_id, u.tg_id
                FROM dont_touch.parking_releases pr
                         JOIN dont_touch.users u ON pr.user_id = u.user_id
                WHERE pr.id = %s
                ''', (release_id,))
    return cur.fetchone()

async def update_parking_releases(cur, user_id, release_id):
    """
    Назначает парковочное место конкретному пользователю.

    Обновляет запись об освобожденном месте, указывая пользователя, который получил
    это место в свое распоряжение.

    Параметры:
        cur: курсор базы данных для выполнения SQL-запросов
        user_id: UUID пользователя, который получает место
        release_id: UUID записи в таблице parking_releases

    Логика:
        - Устанавливает user_id_took = указанному пользователю
        - Превращает свободное место в занятое конкретным пользователем

    Особенности:
        - Критическая операция в процессе распределения мест
        - После выполнения место перестает быть свободным (user_id_took IS NULL → user_id_took = user_id)
        - Асинхронная функция, требует await при вызове
    """
    cur.execute('''
                UPDATE dont_touch.parking_releases
                SET user_id_took = %s
                WHERE id = %s
                ''', (user_id, release_id))

async def update_parking_request_status(cur, request_id):
    """
        Обновляет статус запроса на парковку после успешного распределения места.

        Помечает запрос как выполненный и фиксирует время обработки.

        Параметры:
            cur: курсор базы данных для выполнения SQL-запросов
            request_id: UUID запроса в таблице parking_requests

        Логика:
            - Меняет статус с 'PENDING' на 'ACCEPT'
            - Устанавливает processed_at в текущее время сервера БД

        Особенности:
            - Закрывает цикл обработки запроса
            - CURRENT_TIMESTAMP гарантирует единое время на сервере БД
            - Статус 'ACCEPT' указывает на успешное выполнение запроса
            - Асинхронная функция, требует await при вызове
        """
    cur.execute('''
                UPDATE dont_touch.parking_requests
                SET status       = 'ACCEPT',
                    processed_at = CURRENT_TIMESTAMP
                WHERE id = %s
                ''', (request_id,))


async def update_user_rating(cur, user_id):
    """
    Увеличивает рейтинг пользователя после получения им парковочного места.

    Обновляет счетчик рейтинга пользователя, увеличивая его на 1.

    Параметры:
        cur: курсор базы данных для выполнения SQL-запросов
        user_id: UUID пользователя, чей рейтинг нужно увеличить

    Логика:
        - Инкрементирует значение rating на 1
        - Используется для отслеживания количества полученных мест

    Особенности:
        - Рейтинг используется для приоритизации при будущих распределениях
        - Более высокий рейтинг = меньше приоритет (судя по ORDER BY rating ASC в предыдущих функциях)
        - Асинхронная функция, требует await при вызове
        - Система поощряет справедливость - те, кто получил больше мест, имеют меньший приоритет
    """
    cur.execute('''
                UPDATE dont_touch.users
                SET rating = rating + 1
                WHERE user_id = %s
                ''', (user_id,))